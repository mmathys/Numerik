template <typename Scalar> void fft2(MatrixXcd &C, const MatrixBase<Scalar> &Y) {
	int m = Y.rows(), n = Y.cols();
	C.resize(m,n);
	MatrixXcd tmp(m,n);

	FFT<double> fft; // Helper class for DFT
	// Transform rows of matrix Y
	for (int k = 0; k < m; k++) {
		VectorXcd tv(Y.row(k));
		tmp.row(k) = fft.fwd(tv).transpose();
	}

	// Transform columns of temporary matrix
	for (int k = 0; k < n; k++) {
		VectorXcd tv(tmp.col(k));
		C.col(k) = fft.fwd(tv);
	}
}

template <typename Scalar> void ifft2(MatrixXcd &C, const MatrixBase<Scalar> &Y) {
	int m = Y.rows(), n = Y.cols();
	fft2(C, Y.conjugate());
	C = C.conjugate()/(m*n);
}

void conv2(MatrixXcd &C, const MatrixXcd &A1, const MatrixXcd &A2) {
	// returns discrete convolution between A1 and A2 (fft implementation)
	int n1 = A1.rows();
	int m1 = A1.cols();
	int n2 = A2.rows();
	int m2 = A2.cols();
	int Lrow = n1+n2-1;
	int Lcol = m1+m2-1;

	MatrixXcd A1_ext = MatrixXcd::Zero(Lrow, Lcol);
	MatrixXcd A2_ext = MatrixXcd::Zero(Lrow, Lcol);
	MatrixXcd tmp1 = MatrixXcd::Zero(Lrow, Lcol);
	MatrixXcd tmp2 = MatrixXcd::Zero(Lrow, Lcol);
	A1_ext.topLeftCorner(n1,m1) = A1;
	A2_ext.topLeftCorner(n2,m2) = A2;

	fft2(tmp1, A1_ext);
	fft2(tmp2, A2_ext);
	ifft2(C, tmp1.cwiseProduct(tmp2));
}


